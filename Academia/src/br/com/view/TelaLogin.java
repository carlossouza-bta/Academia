    private void botaoLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLoginActionPerformed
        
        ControleCliente cc = new ControleCliente();
        String senhaCriptografada = DigestUtils.sha256Hex(String.copyValueOf(jTextSenha.getPassword()));
        if(cc.efetuarLogin(jTextUsuario.getText(), senhaCriptografada)) {
            ConsultarClientes consultarClientes = new ConsultarClientes();
            consultarClientes.setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_botaoLoginActionPerformed

